print('Let\'s assign car = \'bmw\'')
car = 'bmw'
print('Checking car == \'bmw\':')
print(car == 'bmw')

print('\nNow let\'s assign car = \'audi\'')
car = 'audi'
print('Checking car == \'bmw\':')
print(car == 'bmw')

print('\nTesting for equality is case sensitive in Python')
print('car = \'Audi\'')
car = 'Audi'
print('Checking car == \'audi\':')
print(car == 'audi')

print('\nCase insensitive test')
print('Checking car.lower() == \'audi\':')
print(car.lower() == 'audi')
